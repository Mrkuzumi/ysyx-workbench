//1、编写一个程序，定义两个类型和长度都相同的数组，将其中一个数组的所有元素拷贝给另一个。既然数组不能直接赋值，想想应该怎么实现。
#include <stdio.h>
int main()
{
int a[5]={1,2,3,4,5};
int b[5]={6,7,8,9,10};
for (int i = 0; i < 5; i++)
{
    b[i] = a[i];
}

printf("数组a的元素为：%d %d %d %d %d\n",a[0],a[1],a[2],a[3],a[4]);
printf("数组b的元素为：%d %d %d %d %d\n",b[0],b[1],b[2],b[3],b[4]);
return 0;
}

//数组不能直接 b = a 赋值，必须逐元素拷贝